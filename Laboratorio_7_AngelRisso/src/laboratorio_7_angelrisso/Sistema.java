    private void bt_aggProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_aggProdActionPerformed
        DefaultComboBoxModel mod
                = (DefaultComboBoxModel) cb_productos.getModel();
        String nombre = tf_nomProd.getText();
        int carga = Integer.parseInt(tf_carga.getText());
        int precio = Integer.parseInt(tf_precio.getText());
        p = new Producto(nombre, precio, carga);
        mod.addElement(p);

        cb_productos.setModel(mod);

        tf_carga.setText("");
        tf_precio.setText("");
        tf_nomProd.setText("");

    }//GEN-LAST:event_bt_aggProdActionPerformed
